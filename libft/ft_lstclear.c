#include "libft.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*t1;
	t_list	*t2;

	if (!lst || !*lst)
		return ;
	t1 = *lst;
	while (t1)
	{
		t2 = t1;
		t1 = t1->next;
		free(t2);
	}
	*lst = NULL;
}
